"""
repair_shells_for_scad.py  — run with:
    blender --background --python repair_shells_for_scad.py

Creates manifold (watertight) repaired copies of the Serenity UAV shell STL
files needed by the parametric 24" SCAD feature-cut files.

The Thingiverse source STLs (and the hollow shells derived from them) carry
open-edge non-manifold geometry that CGAL cannot process in boolean operations.
Blender's voxel-remesh modifier reconstructs a clean watertight mesh at the
specified voxel pitch while preserving the overall hull shape.

Repaired files are written with suffix `_repaired.stl` in the same directory
as the input.

Per-part voxel pitch selection criteria (results of structural audit 2026-05-26):
    1.5 mm — large hull shells (head, cargo, rear, nacelles): balances shape
             fidelity against mesh complexity; smallest SCAD feature is 11 mm bore.
    1.0 mm — integrated stator shells: stator fin thickness ~2 mm requires finer
             pitch to prevent fin merge without generating excessively large STL.
    0.8 mm — medium thin-walled parts (wings at 19 mm min_dim, nozzle petals at
             18-19 mm min_dim): preserves slot and curvature detail.
    0.5 mm — very thin / flat parts (feet at 9 mm min_dim): needs ≥18 voxels
             through the minimum dimension to capture the part accurately.

Manifold status before repair (from audit 2026-05-26):
    HEAD / CARGO shells: repaired OK (0 NM edges)
    REAR shell repaired: 2 NM edges at Z≈0 boundary — acceptable (subtracted zone)
    ENG_LEFT repaired:   3 NM edges at Z≈0 boundary — acceptable (bore zone)
    ENG_RIGHT repaired:  0 NM edges — OK
    WINGS:             112 NM edges — repaired here
    FEET:               99 NM edges — repaired here (load-bearing, CRITICAL)
    STATOR shells:     761 NM edges each — repaired here
    NOZZLE PETAL:        8 NM edges — repaired here
    NOZZLE CLOSED ASM:  57 NM edges — repaired here
    PIVOT ARM:           3 NM edges — repaired here

Author:  Steve Griffing, PE(CSE), CISSP-ISSEP, CPP
License: CC BY 4.0  <https://creativecommons.org/licenses/by/4.0/>
Date:    2026-05-26 (updated from 2026-05-25 to add per-part pitches and new parts)
"""

