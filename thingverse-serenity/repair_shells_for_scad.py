def count_non_manifold(obj):
    """Return the number of non-manifold edges in the active mesh."""
    bpy.context.view_layer.objects.active = obj
    bpy.ops.object.mode_set(mode="EDIT")
    bm = bmesh.from_edit_mesh(obj.data)
    bm.edges.ensure_lookup_table()
    count = sum(1 for e in bm.edges if not e.is_manifold)
    bpy.ops.object.mode_set(mode="OBJECT")
    return count


def repair_shell(filename, voxel_pitch_mm):
    """
    Import, voxel-remesh, and export a repaired copy of the given STL.

    Parameters
    ----------
    filename : str
        Basename of the STL file in SHELL_DIR.
    voxel_pitch_mm : float
        Voxel size for the remesh modifier.  Choose so that the minimum part
        dimension spans at least 12 voxels.
    """
    in_path = os.path.join(SHELL_DIR, filename)
    stem    = filename.replace(".stl", "")
    out_path = os.path.join(SHELL_DIR, stem + "_repaired.stl")

    if not os.path.isfile(in_path):
        print(f"  SKIP — not found: {in_path}")
        return

    print(f"\n=== Repairing {filename}  (pitch={voxel_pitch_mm} mm) ===")

    # Clear scene
    bpy.ops.object.select_all(action="SELECT")
    bpy.ops.object.delete()

    # Import
    bpy.ops.wm.stl_import(filepath=in_path)
    obj = bpy.context.selected_objects[0]
    bpy.context.view_layer.objects.active = obj

    nm_before = count_non_manifold(obj)
    bb = obj.bound_box
    xs = [v[0] for v in bb]
    ys = [v[1] for v in bb]
    zs = [v[2] for v in bb]
    print(f"  Before: non-manifold edges={nm_before}")
    print(f"  Bounds: X={min(xs):.0f}..{max(xs):.0f}  "
          f"Y={min(ys):.0f}..{max(ys):.0f}  Z={min(zs):.0f}..{max(zs):.0f}")

    # Apply voxel remesh — reconstructs a clean watertight mesh from the volume
    mod = obj.modifiers.new("remesh", "REMESH")
    mod.mode = "VOXEL"
    mod.voxel_size = voxel_pitch_mm
    bpy.ops.object.modifier_apply(modifier="remesh")

    nm_after = count_non_manifold(obj)
    bpy.context.view_layer.objects.active = obj
    n_faces = len(obj.data.polygons)
    print(f"  After:  non-manifold edges={nm_after}  faces={n_faces}")

    if nm_after != 0:
        print(f"  WARNING: {nm_after} NM edges remain — consider finer pitch.")

    # Export repaired mesh
    bpy.ops.object.select_all(action="DESELECT")
    obj.select_set(True)
    bpy.context.view_layer.objects.active = obj
    bpy.ops.wm.stl_export(filepath=out_path, export_selected_objects=True)
    sz_kb = os.path.getsize(out_path) // 1024
    print(f"  -> {out_path}  ({sz_kb} KB)")


for shell_filename, voxel_pitch in SHELLS_TO_REPAIR:
    repair_shell(shell_filename, voxel_pitch)

print("\nDone.  Repaired shells written with suffix _repaired.stl")
print("Verify Z-range and non-manifold count in output above before committing.")
