"""
repair_shells_for_scad.py  — run with:
    blender --background --python repair_shells_for_scad.py

Creates manifold (watertight) repaired copies of the pre-computed Serenity UAV
shell STL files needed by the parametric 24" SCAD feature-cut files.

The Thingiverse source STLs (and the hollow shells derived from them) carry
open-edge non-manifold geometry that CGAL cannot process in boolean operations.
Blender's voxel-remesh modifier reconstructs a clean watertight mesh at the
specified voxel pitch while preserving the overall hull shape.

Repaired files are written with suffix `_repaired.stl` in the same directory
as the input.  The SCAD files that reference them are updated to import these
repaired versions instead of trying to re-derive the shell from the raw
Thingiverse STLs (which also require boolean ops on non-manifold geometry).

Voxel pitch: 1.5 mm — preserves hull curvature detail; smallest feature cut
in the SCAD files is an 11 mm sensor bore, so 1.5 mm voxels are adequate.

Author:  Steve Griffing, PE(CSE), CISSP-ISSEP, CPP
License: CC BY 4.0  <https://creativecommons.org/licenses/by/4.0/>
Date:    2026-05-25
"""

import bpy
import bmesh
import os

BASE_DIR = os.path.dirname(os.path.abspath(__file__))
SHELL_DIR = os.path.join(BASE_DIR, "files-hollowed-18in")

# Shells needed for the parametric 24" SCAD feature-cut files.
# These were generated by blender_hollow_shells.py from the Thingiverse sources.
# They are non-manifold because the Thingiverse source STLs carry open edges.
SHELLS_TO_REPAIR = [
    "s_head_shell24.stl",           # used by serenity/stl/s_head_shell24.scad
    "s_cargo_sect_shell24.stl",     # used by serenity/stl/s_cargo_sect_shell24.scad
    "s_rear_shell24.stl",           # used by serenity/stl/s_rear_neck_intake_shell24.scad
    "s_eng_left_shell24_50mm.stl",  # used by serenity/stl/nacelle_pod_50mm_tandem.scad (port)
    "s_eng_right_shell24_50mm.stl", # used by serenity/stl/nacelle_pod_50mm_tandem.scad (stbd)
]

# Voxel pitch for the remesh.
# 1.5 mm: used for most shells — balances shape fidelity against mesh complexity.
#         Nacelle shells use 1.5 mm because their wall thickness is 2.5 mm and the
#         interior cavity (between bore tube and nacelle inner wall) is ~4 mm minimum.
#         Coarser pitch would merge the inner and outer shell faces; finer pitch would
#         generate very large STL files that slow CGAL renders.
VOXEL_PITCH_MM = 1.5


def count_non_manifold(obj):
    """Return the number of non-manifold edges in the active mesh."""
    bpy.context.view_layer.objects.active = obj
    bpy.ops.object.mode_set(mode="EDIT")
    bm = bmesh.from_edit_mesh(obj.data)
    bm.edges.ensure_lookup_table()
    count = sum(1 for e in bm.edges if not e.is_manifold)
    bpy.ops.object.mode_set(mode="OBJECT")
    return count


def repair_shell(filename):
    """Import, voxel-remesh, and export a repaired copy of the given STL."""
    in_path = os.path.join(SHELL_DIR, filename)
    stem = filename.replace(".stl", "")
    out_path = os.path.join(SHELL_DIR, stem + "_repaired.stl")

    if not os.path.isfile(in_path):
        print(f"  SKIP — not found: {in_path}")
        return

    print(f"\n=== Repairing {filename} ===")

    # Clear scene
    bpy.ops.object.select_all(action="SELECT")
    bpy.ops.object.delete()

    # Import
    bpy.ops.wm.stl_import(filepath=in_path)
    obj = bpy.context.selected_objects[0]
    bpy.context.view_layer.objects.active = obj

    nm_before = count_non_manifold(obj)
    bb = obj.bound_box
    xs = [v[0] for v in bb]
    ys = [v[1] for v in bb]
    zs = [v[2] for v in bb]
    print(f"  Before: non-manifold edges={nm_before}")
    print(f"  Bounds: X={min(xs):.0f}..{max(xs):.0f}  "
          f"Y={min(ys):.0f}..{max(ys):.0f}  Z={min(zs):.0f}..{max(zs):.0f}")

    # Apply voxel remesh — reconstructs a clean watertight mesh from the volume
    mod = obj.modifiers.new("remesh", "REMESH")
    mod.mode = "VOXEL"
    mod.voxel_size = VOXEL_PITCH_MM
    bpy.ops.object.modifier_apply(modifier="remesh")

    nm_after = count_non_manifold(obj)
    bpy.context.view_layer.objects.active = obj
    n_faces = len(obj.data.polygons)
    print(f"  After:  non-manifold edges={nm_after}  faces={n_faces}")

    # Export repaired mesh
    bpy.ops.object.select_all(action="DESELECT")
    obj.select_set(True)
    bpy.context.view_layer.objects.active = obj
    bpy.ops.wm.stl_export(filepath=out_path, export_selected_objects=True)
    sz_kb = os.path.getsize(out_path) // 1024
    print(f"  -> {out_path}  ({sz_kb} KB)")


for shell in SHELLS_TO_REPAIR:
    repair_shell(shell)

print("\nDone.  Repaired shells written with suffix _repaired.stl")
print("Update the SCAD files to import the _repaired.stl versions.")
